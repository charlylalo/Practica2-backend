package com.examen.Datos;

public class Datos {
	
	private Double inversionInicial;
	private Double aportacionAnual;
	private Integer porcIncAnual;
	private Integer investYear;
	private Integer rendimientoInv;
	public Double getInversionInicial() {
		return inversionInicial;
	}
	public void setInversionInicial(Double inversionInicial) {
		this.inversionInicial = inversionInicial;
	}
	public Double getAportacionAnual() {
		return aportacionAnual;
	}
	public void setAportacionAnual(Double aportacionAnual) {
		this.aportacionAnual = aportacionAnual;
	}
	public Integer getPorcIncAnual() {
		return porcIncAnual;
	}
	public void setPorcIncAnual(Integer porcIncAnual) {
		this.porcIncAnual = porcIncAnual;
	}
	public Integer getInvestYear() {
		return investYear;
	}
	public void setInvestYear(Integer investYear) {
		this.investYear = investYear;
	}
	public Integer getRendimientoInv() {
		return rendimientoInv;
	}
	public void setRendimientoInv(Integer rendimientoInv) {
		this.rendimientoInv = rendimientoInv;
	}
	
	
}
